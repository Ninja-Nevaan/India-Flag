import turtle

t=turtle.Turtle()

t.penup()
t.goto(300, 150)
t.pendown()
t.fillcolor("orange")
t.begin_fill()
t.left(180)
t.forward(600)
t.left(90)
t.forward(100)
t.left(90)
t.forward(600)
t.left(90)
t.forward(100)
t.end_fill()

t.penup()
t.goto(300, 50)
t.pendown()
t.right(180)
t.forward(100)
t.right(90)
t.forward(600)
t.right(90)
t.forward(100)

t.penup()
t.goto(300, -50)
t.pendown()
t.right(180)
t.fillcolor("green")
t.begin_fill()
t.forward(100)
t.right(90)
t.forward(600)
t.right(90)
t.forward(100)
t.right(90)
t.forward(600)
t.end_fill()

t.penup()
t.goto(0,0)
t.pencolor("blue")
t.pendown()
t.forward(45)
t.left(180)
t.forward(45)
t.left(180)
for i in range(23):
    t.left(15)
    t.forward(45)
    t.left(180)
    t.forward(45)
    t.left(180)
t.left(15)
t.right(180)
t.forward(45)
t.left(90)
t.circle(45)
t.hideturtle()
